  static int nahualIndex(DateTime date) {
    // GMT Korelasyonuna göre (1 Ocak 2000 = 11 Ik' / Ik = İndeks 1)
    final ref = DateTime.utc(2000, 1, 1);
    final d = DateTime.utc(date.year, date.month, date.day);
    final diff = d.difference(ref).inDays;
    int idx = (diff + 1) % 20;
    while (idx < 0) idx += 20;
    return idx;
  }

  static int galacticTone(DateTime date) {
    // 1 Ocak 2000 = 11 Ton. 
    final ref = DateTime.utc(2000, 1, 1);
    final d = DateTime.utc(date.year, date.month, date.day);
    final diff = d.difference(ref).inDays;
    int tone = (diff + 10) % 13;
    while (tone < 0) tone += 13;
    return tone + 1; // 1 ile 13 arası değer
  }

